    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser diretorioDb = new JFileChooser();
        diretorioDb.setFileFilter(new FileNameExtensionFilter("Data Base Files", "fdb", "gdb"));
        diretorioDb.setAcceptAllFileFilterUsed(false);
        diretorioDb.setDialogTitle("Select Database");
        //diretorio.showOpenDialog(this);
        diretorioDb.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int retorno = diretorioDb.showOpenDialog(this);
        if (retorno == JFileChooser.APPROVE_OPTION) {
            File path = diretorioDb.getSelectedFile();
            jTextFieldDataBaseFile.setText(path.getPath());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String strPass = new String(jPasswordFieldAdmPassword.getPassword()).trim();
        if (jTextFieldServerHostName.getText().trim().length() > 3 && jTextFieldPort.getText().trim().length() >= 4 && jTextFieldDataBaseFile.getText().trim().length() >= 8 && jTextFieldUserName.getText().trim().length() >= 4 && strPass.length() > 4) {

            configDb = new ConfigDb(jTextFieldServerHostName.getText(), jTextFieldPort.getText(), jTextFieldDataBaseFile.getText(), jTextFieldUserName.getText(), strPass, jTextFieldDataBaseUrl.getText(), jCheckBox1.isSelected());
            configDb.salvar();
dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Dados invalidos, Preencha corretamente");
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

