    public void carregarConfigDb() {
        try {
            ObjectInputStream configLer = new ObjectInputStream(new FileInputStream("configDb.dat"));
            configDb = (ConfigDb) configLer.readObject();

            jTextFieldServerHostName.setText(configDb.getHost());
            jTextFieldPort.setText(configDb.getPort());
            jTextFieldDataBaseFile.setText(configDb.getDataBaseFile());
            jTextFieldUserName.setText(configDb.getAdmUserNameDb());
            jPasswordFieldAdmPassword.setText(configDb.getPassword());
            jCheckBox1.setSelected(configDb.getRememberPassword());
            jTextFieldDataBaseUrl.setText(configDb.getUrlDb());

        } catch (ClassNotFoundException erro) {
            JOptionPane.showMessageDialog(null, "Erro de classe erro = " + erro);
        } catch (FileNotFoundException erro) {
            JOptionPane.showMessageDialog(null, "O arquivo de configuração: configDb.dat não foi encontrado.: erro = " + erro);

        } catch (IOException erro) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar ler dados no arquivo de configurção: configDb.dat erro= " + erro);
        }
    }

